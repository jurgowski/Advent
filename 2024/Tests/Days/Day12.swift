private func countPerimeter(pos: Pos, dir: Dir, rDir: Dir?, grid: [[String.Element]]) -> Int {
    if pos[dir].inside(map: grid) && grid[pos] == grid[pos[dir]] {
        return 0
    }
    guard let rDir else { return 1 }
    if pos[rDir].inside(map: grid) && grid[pos] == grid[pos[rDir]] &&
        (!pos[rDir][dir].inside(map: grid) || grid[pos[rDir]] != grid[pos[rDir][dir]]) {
            return 0
    }
    return 1
}

private func perimeter(pos: Pos, grid: [[String.Element]], extended: Bool) -> Int {
    return (
        countPerimeter(pos: pos, dir: .nort, rDir: extended ? .west : nil, grid: grid) +
        countPerimeter(pos: pos, dir: .sout, rDir: extended ? .west : nil, grid: grid) +
        countPerimeter(pos: pos, dir: .east, rDir: extended ? .nort : nil, grid: grid) +
        countPerimeter(pos: pos, dir: .west, rDir: extended ? .nort : nil, grid: grid)
    )
}

private struct Region {
    let id: String.Element
    let area: Int
    let perim: Int
    var price: Int { area * perim }
}

private func run(input: String, extendedPerim: Bool) -> Int {
    let grid = input
        .split(separator: "\n")
        .map { Array($0) }
    
    var seen: Set<Pos> = []
    var regions: [Region] = []
    
    for y in grid.indices {
        for x in grid[y].indices {
            if seen.contains(Pos(x, y)) {
                continue
            }
            let pos = Pos(x, y)
            var area = 0
            var perim = 0
            var reachable = [pos]
            while !reachable.isEmpty {
                let next = reachable.removeFirst()
                if grid[next] != grid[pos] || seen.contains(next) {
                    continue
                }
                seen.insert(next)
                area += 1
                perim += perimeter(pos: next, grid: grid, extended: extendedPerim)
                reachable += next.reachable(map: grid)
            }
            regions.append(Region(id: grid[pos], area: area, perim: perim))
        }
    }
    return regions.reduce(0) { $0 + $1.price }
}

