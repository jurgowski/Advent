import Testing

private enum Pattern: CaseIterable {
    case n
    case s
    case e
    case w
    case ne
    case nw
    case se
    case sw
}

private func check(_ chars: [[Substring.Element]], sx: Int, sy: Int, pattern: Pattern) -> Bool {
    var remaining = "XMAS"
    var x = sx
    var y = sy
    while remaining.count > 0 {
        guard y >= 0, y < chars.count, x >= 0, x < chars[y].count else { return false }
        guard remaining.first == chars[y][x] else { return false }
        remaining.removeFirst()
        switch pattern {
        case .e: x += 1
        case .n: y -= 1
        case .w: x -= 1
        case .s: y += 1
        case .ne: x += 1; y -= 1
        case .nw: x -= 1; y -= 1
        case .se: x += 1; y += 1
        case .sw: x -= 1; y += 1
        }
    }
    return true
}

private func masCheck(_ chars: [[Substring.Element]], x: Int, y: Int) -> Bool {
    guard chars[y][x] == "A" else { return false }
    guard y >= 1, y < chars.count - 1, x >= 1, x < chars[y].count - 1 else { return false }
    guard ["M", "S"].contains(chars[y-1][x-1]) else { return false }
    guard ["M", "S"].contains(chars[y+1][x-1]) else { return false }
    guard ["M", "S"].contains(chars[y-1][x+1]) else { return false }
    guard ["M", "S"].contains(chars[y+1][x+1]) else { return false }
    guard chars[y-1][x-1] != chars[y+1][x+1] else { return false }
    guard chars[y-1][x+1] != chars[y+1][x-1] else { return false }
    return true
}

private func part1(input: String) -> Int {
    let chars = input
        .split(separator: "\n")
        .map { Array($0) }

    var matches = 0
    for y in 0..<chars.count {
        for x in 0..<chars[y].count {
            for pattern in Pattern.allCases {
                matches += check(chars, sx: x, sy: y, pattern: pattern) ? 1 : 0
            }
        }
    }
    return matches
}

private func part2(input: String) -> Int {
    let chars = input
        .split(separator: "\n")
        .map { Array($0) }

    var matches = 0
    for y in 0..<chars.count {
        for x in 0..<chars[y].count {
            matches += masCheck(chars, x: x, y: y) ? 1 : 0
        }
    }
    return matches
}

